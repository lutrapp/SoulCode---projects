// 1.Faça um programa que leia um número entre 0 e 10, e escreva este
// número por extenso.

var numero = 8;
switch(numero){
    case 0:
        document.write(numero, ": lê-se zero");
        break;
    case 1:
        document.write(numero, ": lê-se um");
        break;
    case 2:
        document.write(numero, ": lê-se dois");
        break;
    case 3:
        document.write(numero, ": lê-se três");
        break;
    case 4:
        document.write(numero, ": lê-se quatro");
        break;
    case 5:
        document.write(numero, ": lê-se cinco");
        break;
    case 6:
        document.write(numero, ": lê-se seis");
        break;
    case 7:
        document.write(numero, ": lê-se sete");
        break;
    case 8:
        document.write(numero, ": lê-se oito");
        break;
    case 9:
        document.write(numero, ": lê-se nove");
        break;
    case 10:
        document.write(numero, ": lê-se dez");
        break;
    default:
        document.write("O leitor aceita apenas números de 0 a 10.");   
}


